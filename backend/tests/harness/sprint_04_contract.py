def test_session_id_autogenerated_and_persisted(fake_orchestrator_llm: None) -> None:
    """BLOCKING-2: session_id 미전송 → UUID4 자동생성 → done 직전 SessionTurn 저장."""
    from app.main import app  # type: ignore
    from app.services.session.memory_store import get_session_store  # type: ignore

    # 세션 저장소 초기화 (테스트 격리)
    get_session_store().reset_all()

    client = TestClient(app)
    with client.stream("POST", "/copilot/query",
                       json={"query": "AAPL 요약"}) as r:
        header_sid = r.headers.get("x-copilot-session-id")
        body = "".join(chunk.decode() for chunk in r.iter_bytes())
    assert header_sid and len(header_sid) >= 32  # uuid4 hex
    events = _parse_sse(body)
    done = [e for e in events if e["type"] == "done"][-1]
    assert done["session_id"] == header_sid
    # done 직전 SessionTurn 이 저장되어 있어야 한다
    store = get_session_store()
    turns = store.list_turns(header_sid)
    assert len(turns) == 1
    assert turns[0].final_card is not None


