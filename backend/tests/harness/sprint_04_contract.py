def test_event_order_invariant(fake_orchestrator_llm: None) -> None:
    """plan.ready → (step.start → step.token* → step.gate* → step.result)+ → final.card → done."""
    from app.main import app  # type: ignore

    client = TestClient(app)
    with client.stream("POST", "/copilot/query",
                       json={"query": "내 포트폴리오와 SPY 비교"}) as r:
        body = "".join(chunk.decode() for chunk in r.iter_bytes())
    events = _parse_sse(body)
    types = [e["type"] for e in events]
    assert types.count("plan.ready") == 1
    assert types[0] == "plan.ready"
    assert types[-1] == "done"
    assert "final.card" in types
    step_ids = {e["step_id"] for e in events if e.get("step_id")}
    for sid in step_ids:
        if sid == "final":
            continue
        starts = [i for i, e in enumerate(events)
                  if e.get("step_id") == sid and e["type"] == "step.start"]
        results = [i for i, e in enumerate(events)
                   if e.get("step_id") == sid and e["type"] == "step.result"]
        if starts and results:
            assert starts[0] < results[0], f"out-of-order for step {sid}"


